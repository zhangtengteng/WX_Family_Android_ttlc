package com.xweisoft.wx.family.logic.model;

import java.io.Serializable;
import java.util.ArrayList;

import com.google.gson.annotations.SerializedName;

/**
 * 分类Model
 * <一句话功能简述>
 * <功能详细描述>
 * 
 * @author  gac
 * @version  [版本号, 2014-6-18]
 * @see  [相关类/方法]
 * @since  [产品/模块版本]
 */
public class KindItem implements Serializable
{
    /**
     * 注释内容
     */
    private static final long serialVersionUID = 5086188446113720756L;
    
    /**
     * 分类Id
     */
    @SerializedName("id")
    private int id;
    
    /**
    * 二手市场分类id
    */
    @SerializedName("catid")
    private int cateId;
    
    /**
     * 二手市场类型编号
     */
    @SerializedName("type_id")
    private int typeId;
    
    /**
     * 分类名称
     */
    @SerializedName("catname")
    private String name;
    
    /**
     * 资源图片id
     */
    private int resId;
    
    /**
     * 分类标示
     */
    @SerializedName("identify")
    private String identify;
    
    /**
     * 分类编号 0代表最上级分类,其它代表上级分类编号
     */
    @SerializedName("pid")
    private int pid;
    
    /**
     * 层级深度
     */
    @SerializedName("depath")
    private int depth;
    
    /**
     * 子分类数量
     */
    @SerializedName("child_num")
    private int childNum;
    
    @SerializedName("name")
    private String catName;
    
    /**
     * wap加载链接
     */
    @SerializedName("wapurl")
    private String wapUrl;
    
    /**
     * icon图片地址
     */
    @SerializedName("iconurl")
    private String iconUrl;
    
    /**
     * 首页icon图片地址
     */
    @SerializedName("topiconurl")
    private String topIconUrl;
    
    /**
     * 子分类数据列表
     */
    @SerializedName("child")
    private ArrayList<KindItem> kindItemList = new ArrayList<KindItem>();
    
    public int getId()
    {
        return id;
    }
    
    public void setId(int id)
    {
        this.id = id;
    }
    
    /**
     * @return 返回 cateId
     */
    public int getCateId()
    {
        return cateId;
    }
    
    /**
     * @param 对cateId进行赋值
     */
    public void setCateId(int cateId)
    {
        this.cateId = cateId;
    }
    
    /**
     * @return 返回 typeId
     */
    public int getTypeId()
    {
        return typeId;
    }
    
    /**
     * @param 对typeId进行赋值
     */
    public void setTypeId(int typeId)
    {
        this.typeId = typeId;
    }
    
    public String getName()
    {
        return name;
    }
    
    public void setName(String name)
    {
        this.name = name;
    }
    
    public int getResId()
    {
        return resId;
    }
    
    public void setResId(int resId)
    {
        this.resId = resId;
    }
    
    public String getIdentify()
    {
        return identify;
    }
    
    public void setIdentify(String identify)
    {
        this.identify = identify;
    }
    
    public int getPid()
    {
        return pid;
    }
    
    public void setPid(int pid)
    {
        this.pid = pid;
    }
    
    public int getDepth()
    {
        return depth;
    }
    
    public void setDepth(int depth)
    {
        this.depth = depth;
    }
    
    public int getChildNum()
    {
        return childNum;
    }
    
    public void setChildNum(int childNum)
    {
        this.childNum = childNum;
    }
    
    /**
     * @return 返回 wapUrl
     */
    public String getWapUrl()
    {
        return wapUrl;
    }

    /**
     * @param 对wapUrl进行赋值
     */
    public void setWapUrl(String wapUrl)
    {
        this.wapUrl = wapUrl;
    }

    /**
     * @return 返回 iconUrl
     */
    public String getIconUrl()
    {
        return iconUrl;
    }

    /**
     * @param 对iconUrl进行赋值
     */
    public void setIconUrl(String iconUrl)
    {
        this.iconUrl = iconUrl;
    }

    /**
     * @return 返回 topIconUrl
     */
    public String getTopIconUrl()
    {
        return topIconUrl;
    }

    /**
     * @param 对topIconUrl进行赋值
     */
    public void setTopIconUrl(String topIconUrl)
    {
        this.topIconUrl = topIconUrl;
    }

    public ArrayList<KindItem> getKindItemList()
    {
        return kindItemList;
    }
    
    public void setKindItemList(ArrayList<KindItem> kindItemList)
    {
        this.kindItemList = kindItemList;
    }
    
    public String getCatName()
    {
        return catName;
    }
    
    public void setCatName(String catName)
    {
        this.catName = catName;
    }

    
}
