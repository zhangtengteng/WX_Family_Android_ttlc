package com.xweisoft.wx.family.logic.model;

import java.io.Serializable;

import com.google.gson.annotations.SerializedName;

/**
 * 图片实体类
 * 
 * @author  李晨光
 * @version  [版本号, 2014年7月16日]
 * @see  [相关类/方法]
 * @since  [产品/模块版本]
 */
public class ImageItem implements Serializable
{

    /**
     * 注释内容
     */
    private static final long serialVersionUID = 1L;
    
    /**
     * 图片ID
     */
    @SerializedName("imgid")
    private String imageid = "";
    
    /**
     * 原图URL
     */
    @SerializedName("url")
    private String imageUrl = "";
    
    /**
     * 大图URL
     */
    @SerializedName("l_url")
    private String lagreUrl = "";
    
    /**
     * 中图URL
     */
    @SerializedName("m_url")
    private String middleUrl = "";
    
    /**
     * 小图URL
     */
    @SerializedName("s_url")
    private String smallUrl = "";

    /**
     * @return 返回 imageid
     */
    public String getImageid()
    {
        return imageid;
    }

    /**
     * @param 对imageid进行赋值
     */
    public void setImageid(String imageid)
    {
        this.imageid = imageid;
    }

    /**
     * @return 返回 imageUrl
     */
    public String getImageUrl()
    {
        return imageUrl;
    }

    /**
     * @param 对imageUrl进行赋值
     */
    public void setImageUrl(String imageUrl)
    {
        this.imageUrl = imageUrl;
    }

    /**
     * @return 返回 lagreUrl
     */
    public String getLagreUrl()
    {
        return lagreUrl;
    }

    /**
     * @param 对lagreUrl进行赋值
     */
    public void setLagreUrl(String lagreUrl)
    {
        this.lagreUrl = lagreUrl;
    }

    /**
     * @return 返回 middleUrl
     */
    public String getMiddleUrl()
    {
        return middleUrl;
    }

    /**
     * @param 对middleUrl进行赋值
     */
    public void setMiddleUrl(String middleUrl)
    {
        this.middleUrl = middleUrl;
    }

    /**
     * @return 返回 smallUrl
     */
    public String getSmallUrl()
    {
        return smallUrl;
    }

    /**
     * @param 对smallUrl进行赋值
     */
    public void setSmallUrl(String smallUrl)
    {
        this.smallUrl = smallUrl;
    } 
}
